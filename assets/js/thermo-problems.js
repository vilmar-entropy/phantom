var max = 25, //dapat yung max ay exact sa no. of problems e.g. #problem1_max
	min = 1;

for(i=1; i<max; i++) {
	$('#problem1_' + (i+1)).hide();
}

$('#previous').click(function() {
	for(i=0; i<max; i++) {
		if($('#problem1_' + (i+1)).is(':visible')) {
			$('#problem1_' + (i+1)).hide();
			
			if((i+1) == min) {
				$('#problem1_' + max).fadeToggle("slow");
				break;
			} 
			$('#problem1_' + i).fadeToggle("slow");
			break;
		}
	}
	
});

$('#next').click(function() {
	for(i=0; i<max; i++) {
		if($('#problem1_' + (i+1)).is(':visible')) {
			$('#problem1_' + (i+1)).hide();
			
			if((i+1) == max) {
				$('#problem1_' + 1).fadeToggle("slow");
				break;
			} 
			$('#problem1_' + (i+2)).fadeToggle("slow");
			break;
		}
	}
	
});


 
